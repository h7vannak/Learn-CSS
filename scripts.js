function showSidebar(menuKey, clickedElement) {
    // Remove active class from all top menu items
    document.querySelectorAll(".top-menu ul li").forEach(li => li.classList.remove("active"));
    clickedElement.classList.add("active");

    // Show sidebar
    document.getElementById("sidebar").style.display = "block";

    // Get the sidebar menu container
    const sidebarMenu = document.getElementById("sidebar-menu");
    sidebarMenu.innerHTML = ""; // Clear previous items

    // Populate sidebar based on selected top menu
    sidebarData[menuKey].forEach(item => {
        let li = document.createElement("li");
        li.textContent = item.name;
        li.onclick = function () {
            showContent(item.content);
            setActiveSidebarItem(this);
        };
        sidebarMenu.appendChild(li);
    });

    // Show corresponding content for the top menu
    document.querySelectorAll(".content-section").forEach(sec => sec.classList.remove("active"));
    document.getElementById(`content-${menuKey}`).classList.add("active");
}

function showContent(content) {
    // Replace the content section with new content
    document.querySelector(".content").innerHTML = content;
}

function setActiveSidebarItem(selectedItem) {
    // Remove active class from all sidebar items
    document.querySelectorAll(".sidebar ul li").forEach(li => li.classList.remove("active"));
    selectedItem.classList.add("active");
}

// Auto-Load the first menu (Menu 1) when the page loads
window.onload = function() {
    document.querySelector(".top-menu ul li").click();
};